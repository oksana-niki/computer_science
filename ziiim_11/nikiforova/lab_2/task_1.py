"""
3.2.1 Задача 1: “Змейка”

Создайте матрицу 10 на 10 в виде списка списков и разместите в ней
последовательные числа по возрастанию, так, чтобы они в первой строке
следовали слева направо, во второй строке — справа налево,
в третьей — слева направо, и т. д.

Выведите в консоли то, что у вас получилось.
"""
# -*- coding: utf-8 -*-

# Размер матрицы
rows = 10
cols = 10

# Счётчик чисел
counter = 0

# Пустая матрица
matrix = []

# Заполняем матрицу построчно
for i in range(rows):
    row = []
    for j in range(cols):
        row.append(counter)
        counter += 1
    # Для чётных строк (0, 2, 4...) — оставляем порядок
    # Для нечётных строк (1, 3, 5...) — переворачиваем строку
    if i % 2 == 1:
        row.reverse()
    matrix.append(row)

# Выводим результат в виде таблицы
# ширина числа == 2
for row in matrix:
    print("  ".join(f"{num:2}" for num in row))
